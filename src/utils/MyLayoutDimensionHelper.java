package utils;


public class MyLayoutDimensionHelper {
	static Integer displaywidth;
	static Integer displayheight;
	public static Integer getDisplaywidth() {
		return displaywidth;
	}
	public static void setDisplaywidth(Integer displaywidth) {
		MyLayoutDimensionHelper.displaywidth = displaywidth;
	}
	public static Integer getDisplayheight() {
		return displayheight;
	}
	public static void setDisplayheight(Integer displayheight) {
		MyLayoutDimensionHelper.displayheight = displayheight;
	}



}
